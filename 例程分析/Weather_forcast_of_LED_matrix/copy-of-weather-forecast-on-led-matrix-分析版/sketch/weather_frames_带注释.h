const uint32_t snowy[][5] = {
    {0x0780c208, 0x084041fc, 0x02004400, 0x88000000, 650},
    {0x0780c208, 0x084041fc, 0x00201002, 0x20000000, 650},
    {0x0780c208, 0x084041fc, 0x08800100, 0x80000000, 650},
};

